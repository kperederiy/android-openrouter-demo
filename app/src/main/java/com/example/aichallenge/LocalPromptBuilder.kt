package com.example.aichallenge


class LocalPromptBuilder {


    fun buildRagPrompt(

        question: String

    ): String {


        return """

Ты локальный AI ассистент RAG.

Твоя задача:
отвечать только используя предоставленный контекст.

Правила:

1. Не используй знания вне контекста.
2. Не придумывай источники.
3. Если информации недостаточно:
ответь строго:

"Не знаю. Пожалуйста, уточните вопрос."

4. Каждый ответ должен иметь формат:


Ответ:
<текст ответа>


Источники:
- source
- section
- chunkId


Цитаты:
- короткая цитата из контекста



Вопрос пользователя:

$question


Контекст будет добавлен перед вопросом.

        """.trimIndent()

    }

}