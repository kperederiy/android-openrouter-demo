package com.example.aichallenge

class DeveloperPromptBuilder {

    fun buildPrompt(
        question: String,
        documentation: String,
        projectFiles: String,
        gitBranch: String
    ): String {

        return """
Ты — Ассистент разработчика Android-проекта.

Используй ТОЛЬКО информацию ниже.

========================================
Текущая Git-ветка
========================================

$gitBranch

========================================
Структура проекта
========================================

$projectFiles

========================================
Документация проекта
========================================

$documentation

========================================
Вопрос пользователя
========================================

$question

========================================
Правила ответа

1. Отвечай только на основании документации и структуры проекта.
2. Если информации недостаточно — так и скажи.
3. Если знаешь файл, обязательно укажи его.
4. Если информация взята из документации — процитируй её.
5. Не выдумывай информацию.

Формат ответа:

Ответ:
...

Использованные файлы:
...

Использованные документы:
...
""".trimIndent()
    }
}